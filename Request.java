package utilities;

public class Request {
    int src,dest,srtTime,endTime;

    public int getSrc() {
        return src;
    }

    public void setSrc(int src) {
        this.src = src;
    }

    public int getDest() {
        return dest;
    }

    public void setDest(int dest) {
        this.dest = dest;
    }

    public int getSrtTime() {
        return srtTime;
    }

    public void setSrtTime(int srtTime) {
        this.srtTime = srtTime;
    }

    public int getEndTime() {
        return endTime;
    }

    public void setEndTime(int endTime) {
        this.endTime = endTime;
    }
}
